package edu.kgu.recipe.bean;

public class ingredientFormBean {
	private String ingredientname = "";
	private String ingredientamount = "";
	private String ingredientnameIn = "";
	private String ingredientamountIn = "";
	
	public String getIngredientname() {
		return ingredientname;
	}
	public void setIngredientname(String ingredientname) {
		this.ingredientname = ingredientname;
	}
	public String getIngredientamount() {
		return ingredientamount;
	}
	public void setIngredientamount(String ingredientamount) {
		this.ingredientamount = ingredientamount;
	}
	public String getIngredientnameIn() {
		return ingredientnameIn;
	}
	public void setIngredientnameIn(String ingredientnameIn) {
		this.ingredientnameIn = ingredientnameIn;
	}
	public String getIngredientamountIn() {
		return ingredientamountIn;
	}
	public void setIngredientamountIn(String ingredientamountIn) {
		this.ingredientamountIn = ingredientamountIn;
	}

}
